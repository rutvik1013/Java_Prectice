public class Literals
{
    public static void main(String[] args) {
        int a=34;
        byte b=10;
        float c=6.8f;
        double d=1908.78;     // These Are known as a Literals
        long e=788979888L;
        short f=18;
        char g='r';
        boolean h=true;

        String name="Babariya Rutvik";
        System.out.println(name);
    }
}
